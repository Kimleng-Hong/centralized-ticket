    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request, $id)
    {
        $purchase = New TicketPurchase();
        $ticket = Ticket::find($id);

        $purchase->user_id = Auth::user()->id;
        $purchase->ticket_id = $ticket->id;
        $purchase->ref_number = rand(10000000,99999999);
        $purchase->purchase_date = $request->input('purchase_date');
        
        $purchase->save();
        return redirect('/home')->with('status', 'Your Purchase is completed!');
    }

